package WhileLoop;
//while loop

public class WhileLoop {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
            System.out.println("i-ni 1 vahid artirdiqda.Kecirik serti novbeti defe" + i + "ucun yoxlamaga");
        }
        int j = 1;
        while (j <= 5) {
            System.out.println(j);
            j++;
            System.out.println("j-ni 1 vahid artirdiqdeciirik serti novbeti defe" + j + "ucun yoxlamaga");
        }
    }

}
